# if block at basic to advanced level
# basic level
a = 10
if a > 5:
    print("a is greater than 5")    
# advanced level
b = 20
if b > 15:
    print("b is greater than 15")   
if b > 25:
    print("b is greater than 25")
# explanation: In the basic level, we check if the variable 'a' is greater than 5 and print a message if the condition is true. In the advanced level, we check two conditions for the variable 'b'. The first condition checks if 'b' is greater than 15, which is true, so it prints a message. The second condition checks if 'b' is greater than 25, which is false, so it does not print anything. 
