# enscape strings with escape sequences
print("Hello\nWorld")  # Newline    
print("Tab\tSeparated")  # Tab
print("Backslash\\")  # Backslash
print("Single Quote: \'")  # Single Quote
print("Double Quote: \"")  # Double Quote
print("Carriage Return\rOverwrite")  # Carriage Return
print("Unicode Character: \u03A9")  # Unicode Character (Omega)
print("Octal Value: \101")  # Octal Value (A)
print("Hexadecimal Value: \x41")  # Hexadecimal Value (A)
# Raw string to ignore escape sequences
print(r"Raw String: \n \t \\")  # Raw String
